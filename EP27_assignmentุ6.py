#แสดงแม่ 2,3

##########################
for x in range (2,13):
    print("แม่สูตรคูณ :",x)
    for y in range(1,13):
        print(x,"คูณ",y," = ",(x*y))
##########################
start = int(input("ป้อนแม่สูตรคูณเริ่มต้น = "))
stop = int(input("ป้อนแม่สูตรคูณสุดท้าย = "))

for x in range (start,stop+1):
    print("แม่สูตรคูณ :",x)
    for y in range(1,13):
        print(x,"คูณ",y," = ",(x*y))